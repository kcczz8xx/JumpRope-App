"use client";

import { useSidebar } from "@/context/SidebarContext";
import AppHeader from "@/layout/private/AppHeader";
import AppSidebar from "@/layout/private/AppSidebar";
import Backdrop from "@/layout/private/Backdrop";
import React from "react";
import { usePathname } from "next/navigation";

export default function AdminLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const { isExpanded, isHovered, isMobileOpen } = useSidebar();
  const pathname = usePathname();

  // Performance monitoring
  const startTime = performance.now();
  console.log(`âš¡ Performance: Layout render started at ${startTime.toFixed(2)}ms`);

  // Responsive design testing log
  console.log(`ðŸ“± Responsive: isExpanded=${isExpanded}, isHovered=${isHovered}, isMobileOpen=${isMobileOpen}, pathname=${pathname}`);

  // Route-specific styles for the main content container
  const getRouteSpecificStyles = () => {
    switch (pathname) {
      case "/text-generator":
        return "";
      case "/code-generator":
        return "";
      case "/image-generator":
        return "";
      case "/video-generator":
        return "";
      default:
        return "p-4 mx-auto max-w-(--breakpoint-2xl) md:p-6";
    }
  };

  // Dynamic class for main content margin based on sidebar state
  const mainContentMargin = isMobileOpen
    ? "ml-0"
    : isExpanded || isHovered
    ? "xl:ml-[290px]"
    : "xl:ml-[90px]";

  console.log(`ðŸ“± Responsive: mainContentMargin=${mainContentMargin}`);

  // Performance monitoring - layout render complete
  const endTime = performance.now();
  console.log(`âš¡ Performance: Layout render completed in ${(endTime - startTime).toFixed(2)}ms`);

  return (
    <div className="min-h-screen xl:flex">
      {/* Sidebar and Backdrop */}
      <AppSidebar />
      <Backdrop />
      {/* Main Content Area */}
      <div
        className={`flex-1 transition-all  duration-300 ease-in-out ${mainContentMargin}`}
      >
        {/* Header */}
        <AppHeader />
        {/* Page Content */}
        <div className={getRouteSpecificStyles()}>{children}</div>
      </div>
    </div>
  );
}
